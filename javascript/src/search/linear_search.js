/**
 * 線形探索（リニアサーチ）を用いて配列からターゲットを探します。
 * 
 * 線形探索は最も単純な探索アルゴリズムで、配列の先頭から順番に
 * 各要素をターゲットと比較していきます。
 * 
 * 特徴:
 * - ソートされていない配列でも使用可能
 * - 実装が簡単
 * - 小さな配列では効率的
 * - 大きな配列では2分探索などの方が効率的
 * 
 * 時間計算量: O(n) - 最悪の場合、全要素を確認する必要がある
 * 空間計算量: O(1) - 追加のメモリをほとんど使用しない
 * 
 * @param {Array} array 探索対象の配列（ソートされていなくてもよい）
 * @param {*} target 探索する値
 * @returns {number} 見つかった場合はそのインデックス（0から始まる）、見つからない場合は -1
 */
function linearSearch(array, target) {
    // 配列の先頭（インデックス0）から末尾まで順番に探索
    for (let i = 0; i < array.length; i++) {
        // 現在の要素がターゲットと一致するか確認
        // 厳密等価演算子（===）を使用して、型も含めて比較
        if (array[i] === target) {
            // 一致した場合、そのインデックスを返して探索終了
            return i;
        }
    }

    // 配列の全要素を確認しても見つからなかった場合は -1 を返す
    // -1 は「見つからなかった」ことを示す慣例的な値
    return -1;
}

module.exports = { linearSearch };
